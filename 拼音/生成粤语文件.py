# F部分代码

# 打开 yueyu_temp2_Unihan_Readings.txt 文件
with open("yueyu_temp2_Unihan_Readings.txt", "r", encoding="utf-8") as f:
    # 读取文件内容
    lines = f.readlines()

# 处理每一行
for i in range(len(lines)):
    line = lines[i].strip().split('\t')  # 去除首尾空白并按制表符分割
    
    if len(line) == 2 and line[0].startswith("U+"):
        unicode_char = chr(int(line[0][2:], 16))  # 将U+xxxx的内容转换为字符
        line[1] = line[1][:-1] + "q" * int(line[1][-1])  # 将最后一个数字替换为相应数量的“q”
        
        # 更新行内容
        lines[i] = f"{unicode_char}\t{line[1]}\n"

# 写入新文件
with open("yueyu_temp3_Unihan_Readings.txt", "w", encoding="utf-8") as f:
    for line in lines:
        f.write(line)


# 去掉#号、空行制表符为一空格并反转方向

# 输入文件路径和输出文件路径
input_file_path = 'yueyu_temp3_Unihan_Readings.txt'
output_file_path = 'yueyu_temp4_Unihan_Readings.txt'

# 打开输入文件并处理
with open(input_file_path, 'r', encoding='utf-8') as input_file:
    # 打开输出文件并逐行处理写入
    with open(output_file_path, 'w', encoding='utf-8') as output_file:
        for line in input_file:
            # 去掉开头有#号的行，并将制表符替换为一个空格，并去掉空行
            if not line.startswith('#') and line.strip():
                parts = line.strip().split('\t')
                if len(parts) == 2:
                    # 调换“汉字”和“码”的位置
                    reversed_line = parts[1] + ' ' + parts[0]
                    output_file.write(reversed_line + '\n')

print(f'处理完成，结果保存在 {output_file_path} 文件中。')


